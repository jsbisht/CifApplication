eduGapReason VARCHAR(250),
FOREIGN KEY (CID) REFERENCES CANDIDATE(CID));

CREATE TABLE EMPLOYMENT_DETAILS (
CID INT(5) NOT NULL, 
EMPLOYMENTNO VARCHAR(10), 
ORGANIZATIONNAME VARCHAR(100), 
BRANCHADDRESS VARCHAR(20), 
EMPLOYEEID varchar(20) NOT NULL, 
EMPLOYMENT_PERIOD_START DATE,
EMPLOYMENT_PERIOD_END DATE, 
LASTDESIGNATION  VARCHAR(100) NOT NULL, 
LASTDRAWNSALARY VARCHAR(100), 
REPORTEENAME VARCHAR(100),
REPORTEEDESIG VARCHAR(100),
REPORTEECONTACTNO BIGINT,
LEAVINGREASON VARCHAR(100), 
SEPARATIONMODE VARCHAR(100) NOT NULL, 
FOREIGN KEY (CID) REFERENCES CANDIDATE(CID));

CREATE TABLE EMPLOYMENT_BREAK (
CID INT(5) NOT NULL, 
EMPLOYMENT_BREAK_FROM DATE,
EMPLOYMENT_BREAK_TO DATE, 
EMPLOYMENT_BREAK_REASON  VARCHAR(200), 
STUDY_EMPLOYMENT_BREAK_FROM DATE,
STUDY_EMPLOYMENT_BREAK_TO DATE, 
STUDY_EMPLOYMENT_BREAK_REASON  VARCHAR(200), 
