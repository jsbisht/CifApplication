CREATE DATABASE `cif_test` /*!40100 DEFAULT CHARACTER SET utf8 */;
use cif_test_db;
create table Candidate( CID int(5) primary key AUTO_INCREMENT, 
EmployeeName varchar(30) not null, 
EmployeeId varchar(7) default null);

CREATE TABLE PERSONAL_DETAILS( CID INT(5) PRIMARY KEY,
TITLE VARCHAR(4) NOT NULL, 
FIRSTNAME VARCHAR(25) NOT NULL, 
MIDDLENAME VARCHAR(25), 
LASTNAME VARCHAR(25),
GENDER CHAR,
DOB DATE NOT NULL,
PLACE_OF_BIRTH VARCHAR(30) NOT NULL,
MOB_NUMBER BIGINT NOT NULL,
ALT_MOB_NUM BIGINT,
PERSONAL_EMAILID VARCHAR(30),
NATIONALITY VARCHAR(15) NOT NULL,
PASSPORT_NO VARCHAR(15) NOT NULL,
PAN_NO VARCHAR(15) NOT NULL,
NASSCOM_NSR_ITPIN VARCHAR(50) DEFAULT NULL,
FATHER_NAME VARCHAR(25) NOT NULL,
FATHER_DOB DATE NOT NULL,
MOTHER_NAME VARCHAR(25) NOT NULL,
MOTHER_DOB DATE NOT NULL,
SPOUSE_NAME VARCHAR(25) NOT NULL,
SPOUSE_DOB DATE NOT NULL,
CHILD_DETAILS varchar(500),
FOREIGN KEY (CID) REFERENCES CANDIDATE(CID));

CREATE TABLE ADDRESS_DETAILS(
CID INT(5) NOT NULL,
ADDRESS_TYPE CHAR NOT NULL,
ADDRESS VARCHAR(1000) NOT NULL,
CITY VARCHAR(20),
STATE VARCHAR(20),
LANDMARK VARCHAR(30),
STAY_FROM DATE,
STAY_TILDATE DATE, 
PINCODE INT(6) NOT NULL,
TELEPHONE BIGINT,
DURATION varchar(100),
FOREIGN KEY (CID) REFERENCES CANDIDATE(CID));

CREATE TABLE EMERGENCY_DETAILS (

CID INT(5) NOT NULL, 
BLOODGROUP VARCHAR(6), 
ALLERGICTO VARCHAR(100), 
BLOODPRESSURE VARCHAR(20), 
EYESIGHTLEFT VARCHAR(20), 
