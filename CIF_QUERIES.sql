CREATE TABLE EMPLOYMENT_REFERENCE (
CID INT(5) NOT NULL, 
REF_NO INT(1) NOT NULL, 
REF_NAME VARCHAR(20) NOT NULL, 
REF_DESIGNATION VARCHAR(30), 
REF_ORG VARCHAR(100),
REF_CONTACT_NO INT(20), 
