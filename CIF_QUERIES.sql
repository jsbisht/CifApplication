EMER_CONT_NAME VARCHAR(100) NOT NULL, 
EMER_CONT_ADDR_PH VARCHAR(100) NOT NULL, 
EMER_CONT_PH BIGINT NOT NULL,
FOREIGN KEY (CID) REFERENCES CANDIDATE(CID));

CREATE TABLE REFERENCE_DETAILS (
CID INT(5) NOT NULL, 
REF_NAME varchar(30), 
DESIGNATION VARCHAR(100), 
ORGANIZATION VARCHAR(100), 
CONTACT BIGINT, 
EMAIL VARCHAR(100), 
ACQUAINTANCE_PERIOD  VARCHAR(100), 
FOREIGN KEY (CID) REFERENCES CANDIDATE(CID));

CREATE TABLE EDUCATION_DETAILS (
CID INT(5) NOT NULL, 
CLASSLEVEL VARCHAR(18) NOT NULL, 
SCHOOL_NAME VARCHAR(100) NOT NULL, 
BOARD VARCHAR(30) NOT NULL, 
